def test_config_ui_run_portfolio_review_endpoint(monkeypatch, tmp_path) -> None:
    from config_ui import app as config_app

    config_path = tmp_path / "config.yml"
    config_path.write_text("investor_currency: USD\n", encoding="utf-8")
    monkeypatch.setattr(config_app, "CONFIG_PATH", config_path)

    calls: list[list[str]] = []

    def fake_run(cmd, **kwargs):
        calls.append(cmd)
        class Result:
            returncode = 0
            stdout = "dry-run ok"
            stderr = ""

        return Result()

    monkeypatch.setattr(config_app.subprocess, "run", fake_run)

    response = config_app.app.test_client().post(
        "/run-portfolio-review",
        json={"dry_run": True},
    )
    assert response.status_code == 200
    payload = response.get_json()
    assert payload["success"] is True
    assert "--dry-run" in calls[0]
